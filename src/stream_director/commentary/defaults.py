# Ключи таблицы prompts.
RESPONSE_FORMAT_KEY = "response_format"


def game_base_key(game: str) -> str:
    return f"game_base:{game}"


def default_prompt(key: str) -> str:
    """Заводской текст по ключу; неизвестный ключ — пустая строка."""
    if key == RESPONSE_FORMAT_KEY:
        return RESPONSE_FORMAT
    if key.startswith("game_base:"):
        return GAME_BASE.get(key.split(":", 1)[1], "")
    return ""
