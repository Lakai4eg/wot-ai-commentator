def fallback_line(stimulus: Stimulus) -> str:
    key = stimulus.type
    if key == "damage_received" and stimulus.payload.get("from_arta"):
        key = "damage_received_arta"
    if key == "base_capture":
        # сторона захвата меняет смысл фразы на противоположный
        side = stimulus.payload.get("side")
        key = "base_capture_ours" if side == "ours" else "base_capture_theirs"
    options = _TEMPLATES.get(key)
    if not options:
        return "Без комментариев."
    return random.choice(options)
